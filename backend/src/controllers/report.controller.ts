// List all reports generated by the current user
export const listMyReports = async (
  req:  Request,
  res:  Response,
  next: NextFunction
): Promise<void> => {
  try {
    const reports = await prisma.report.findMany({
      where:   { generatedById: req.user!.id },
      orderBy: { generatedAt: 'desc' },
    })
    sendSuccess(res, reports)
  } catch (err) { next(err) }
}

// List all reports (Admin only)
export const listAllReports = async (
  _req: Request,
  res:  Response,
  next: NextFunction
): Promise<void> => {
  try {
    const reports = await prisma.report.findMany({
      orderBy: { generatedAt: 'desc' },
      take:    200,
    })
    sendSuccess(res, reports)
  } catch (err) { next(err) }
}
