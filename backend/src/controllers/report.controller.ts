// Generate an institutional Excel report (Admin only)
export const generateInstitutionalExcel = async (
  req:  Request,
  res:  Response,
  next: NextFunction
): Promise<void> => {
  try {
    const fileUrl = await reportService.generateInstitutionalExcel(
      req.user!.id,
      {
        department: req.query.department as string | undefined,
        year:       req.query.year ? parseInt(req.query.year as string) : undefined,
      }
    )
    sendSuccess(res, { fileUrl }, 'Institutional report generated')
  } catch (err) { next(err) }
}

