import { Request, Response, NextFunction } from 'express'
import * as reportService from '@/services/report.service'
import { sendSuccess } from '@/utils/apiResponse'
import { prisma } from '@/config/db'

// Generate a logbook summary PDF for a student
export const generateLogbookPdf = async (
  req:  Request,
  res:  Response,
  next: NextFunction
): Promise<void> => {
  try {
    // Admins and supervisors can pass a studentId;
    // students always generate their own report
    const rawId = req.params.studentId ?? req.user!.id
    const targetStudentId = Array.isArray(rawId) ? rawId[0] : rawId

    // Resolve to StudentProfile.id if what we have is a User.id
    let profileId = targetStudentId
    const profile = await prisma.studentProfile.findUnique({ where: { userId: targetStudentId } })
    if (profile) profileId = profile.id

    const fileUrl = await reportService.generateLogbookPdf(profileId, req.user!.id)
    sendSuccess(res, { fileUrl }, 'Logbook PDF generated')
  } catch (err) { next(err) }
}

